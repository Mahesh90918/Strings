package com.subString;

public class Substring {
public static void main(String[] args) {
	String s="mahesh is a good boy";
	System.out.println("orginal string :"+s);
	System.out.println("sub string :"+s.substring(6));// printing  from 6 index 
	System.out.println("sub string :"+s.substring(0, 6));// now print only the value between 0 to 6
}
}
