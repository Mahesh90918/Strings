package importantpro;

public class example2 {
	public static void main(String[] args) {
		String s = "mahesh is good boy";
		char[] ch = s.toCharArray();
		// reverse printing array
		for (int i = ch.length - 1; i >= 0; i--) {
			System.out.print(ch[i]);
		}
		System.out.println();

		
		
	}
}
